import { Toaster } from "@/components/ui/toaster";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Routes, Route } from "react-router-dom";
import HomePage from "./pages/HomePage";
import Index from "./pages/Index";
import RealTimeMonitoring from "./pages/RealTimeMonitoring";
import EnhancedRealTimeMonitoring from "./pages/EnhancedRealTimeMonitoring"; // Import the new component
import NotFound from "./pages/NotFound";

const queryClient = new QueryClient();

const App = () => (
  <QueryClientProvider client={queryClient}>
    <TooltipProvider>
      <Toaster />
      <Sonner />
      <BrowserRouter>
        <Routes>
          <Route path="/" element={<HomePage />} />
          <Route path="/simulator" element={<Index />} />
          <Route path="/monitoring" element={<RealTimeMonitoring />} />
          <Route path="/enhanced-monitoring" element={<EnhancedRealTimeMonitoring />} /> {/* New route */}
          <Route path="*" element={<NotFound />} />
        </Routes>
      </BrowserRouter>
    </TooltipProvider>
  </QueryClientProvider>
);

export default App;
